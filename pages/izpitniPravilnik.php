	<h4 align="center">17. Člen</h4><br>
	<p>Kandidat za uspešno opravljen izpit mora osvojiti pozitivno oceno na vseh šest karate področjih in sicer: v katah, dogovorjenem sparingu, prostem sparingu, delo z rekviziti in teoriji, ki je povezana s karate temami iz izpitnih programov.
	</p><br>
	<h4 align="center">18. Člen</h4><br>
	<p>Kandidat za 6. Dan in višje dela izpite pod posebnimi pogoji, ki so v skladu s svetovnimi karate federacijami in sicer: JKA, ITKF, SKIF, WKSA, FUDOKAN standardni in niso zajeti s tem pravilnikom.
	</p><br><br>
