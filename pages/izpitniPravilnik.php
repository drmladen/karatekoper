	<p class="font-size"><br>Na podlagi 11. In 20. člena pravilnika Karate kluba Koper je izvršni odbor na svoji seji dne 21.03.2011 s spremembami in dopolnitvi sprejel naslednji</p><br><br>
	<h1 align="center">IZPITNI PRAVILNIK<br> KARATE KLUBA KOPER</h1><br><br>
	<h3><strong>I. SPLOŠNE DOLOČBE</strong></h3><br>
	<h4 align="center"><strong>1. Člen</strong></h4><br>
	<p>Ta pravilnik določa izpitne pogoje članov Karate kluba Koper (v nadaljevanju KK Koper) in zunanjih članov, ki se želijo pod enakimi pogoji prijaviti in opraviti izpit KYU ter DAN pas pri izpitni komisiji (v nadaljevanju IK) KK Koper.
	</p><br>
	<h4 align="center"><strong>2. Člen</strong></h4><br>
	<p>K izpitom lahko pristopi tudi zunanji član, če izpolnjuje pogoje iz 3., 4. in 5. Člena tega pravilnika.
	</p><br><br>
