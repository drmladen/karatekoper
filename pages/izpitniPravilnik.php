	<h3><strong>III. POGOJI ZA PRIJAVO NA IZPIT</strong></h3><br>
	<h4>A) ŠOLSKI PASOVI</h4><br>
	<h4 align="center"><strong>3. Člen</strong></h4><br>
	<p>Pogoji, da se kandidat prijavi na izpit za vsak KYU oziroma DAN so različni glede na število opravljenih treningov, starosti, zavzetosti na treningih, rezultatov s tekmovanj, udeležbah na tekmovanjih, izobraževanjih, reprezentančnih pripravah, udeležbah na karate šolah in seminarjih.
	</p><br>
	<h4 align="center">4. Člen</h4><br>
	<p><strong><u>V število treningov</u></strong> se šteje: tekmovanja, reprezentančne priprave, sodelovanje na seminarjih in karate šolah.<strong> Eno tekmovanje šteje kot <u>(3) trije treningi</u>. En <u>(1) trening</u> na reprezentančnih pripravah kot dva <u>(2) treninga</u>. Za sodelovanje na seminarjih in karate šolah se dnevno pridobijo <u>(3) trije treningi</u>.</strong>
	</p><br>
	<h4 align="center"><strong>5. Člen</strong></h4><br>
	<p>Kandidati, ki zaradi različnih razlogov začasno prenehajo s treningi <u>(3 mesece in več)</u>, po vnovičnem pričetku se trenažni staž začne z prvim treningom, mora pa izpolniti potrebno časovno obdobje in število treningov, ki so predpisani za določeno stopnjo.
	</p><br>
	<h4 align="center"><strong>6. Člen</strong></h4><br>
	<p>Zunanji kandidati pri prijavi na izpit morajo IK predložiti naslednja dokazila: fotokopijo diplome o opravljenem zadnjem izpitu, dokazilo o prisotnosti na treningih od zadnjega izpita do vključno prijave na izpit ter dokazilo o plačilu izpitne takse.
	</p><br>
	<h4 align="center"><strong>7. Člen</strong></h4><br>
	<p>Na izpit za višje nazive v KK Koper se lahko prijavijo vsi člani društva in kandidati, ki so člani Karate Zveze Slovenije in drugih karate klubov ter zvez, ki sprejmejo ta pravilnik in kateri izpolnjujejo s tem pravilnikom predpisane pogoje.
	</p><br>
	<img width="100%" src="/test/Nova_stran/images/tabela3.png"><br><br>
	<h4>B) MOJSTRSKI PASOVI</h4><br>
	<h4 align="center"><strong>8. Člen</strong></h4><br>
	<p>Kandidati za DAN pasove, 1. In 2. DAN, opravljajo izpit pod naslednjimi pogoji: kandidat je aktiven v klubu kot tekmovalec (mlajši kandidati), poleg rednega treniranja je aktiven kot funkcionar kluba, izvaja delo v izvršnem odboru ali kakšni drugi funkciji v klubu (starejši kandidati), je državni ali mednarodni sodnik, permanentno se izobražuje na področju karateja ali športa nasploh in ima za to ustrezna pisna dokazila, katera fotokopirana predloži IK ob prijavi na izpit.
	</p><br>
	<h4 align="center"><strong>9. Člen</strong></h4><br>
	<p>Kandidati za 3. DAN mora samostojno voditi klub ali biti aktivni trener v matičnem klubu. Dokazila o tem v pisni obliki poda IK ob prijavi na izpit.
	</p><br>
	<h4 align="center"><strong>10. Člen</strong></h4><br>
	<p>Kandidat za 4. DAN mora obenem biti glavni trener v klubu vsaj za eno tekmovalno kategorijo (od kadetov naprej) ali eno tekmovalno disciplino npr. Kate ali športne borbe. Njegov tekmovalec mora biti vsaj državni prvak v eni karate disciplini.
	</p><br>
	<img width="100%" src="/test/Nova_stran/images/tabela4.png"><br><br>
	<h4>C) ČASTNI PASOVI</h4><br>
	<h4 align="center">11. Člen</h4><br>
	<p>IK lahko dodeli častne nazive. Častni nazivi se načeloma dodelijo za izjemen prispevek karateju. Pobudo za dodelitev častnih nazivov podajo posamezni člani kluba ali društva, člani izvršnega odbora KK Koper ali predsednik izvršnega odbora KK Koper. Predlog za dodelitev častnega naziva vsebuje podrobno obrazložitev razloga dodelitve. IK je dolžna v pisni obliki odgovoriti na zahtevek predlagatelja. Imetnik častne diplome ni upravičen do pravic predvidenih za imetnike diplom pridobljenih z izpitnim sistemom.
	</p><br>
	<h4 align="center">12. Člen</h4><br>
	<p>Častni pas se dodeli samo enkrat v karate karieri posameznika. Kandidat za naziv častnega pasa lahko dobi največ častni pas 1. DAN, če pa kandidat že ima DAN pas lahko mu se dodeli ena stopnja več.
	</p><br><br>
	<h4>IV. IZPITNA KOMISIJA</h4><br>
	<h4 align="center">13. Člen</h4><br>
	<p>IK je strokovno telo Izvršnega odbora KK Koper (v nadaljevanju IO). Cilj IK je dvig nivoja in kvalitete šolskih in mojstrskih stopenj pri tem upoštevajoč načela, ki temeljijo na znanstvenih raziskavah. IK ima svoje naloge, ki so opisane v 13. Členu tega pravilnika.
	</p><br>
	<h4 align="center">14. Člen</h4><br>
	<p>Naloge IK so:</p><br>
	<ul class="bulen">
		<li>» priprava in usposabljanje strokovnih delavcev, ki bodo pripravljali kandidate na izpit,</li><br>
		<li>» določa število izpraševalcev za določene stopnje,</li><br>
		<li>» priprava izpitnih programov,</li><br>
		<li>» organiziranje izpitov za šolske (KYU) in mojstrske (DAN) stopnje,</li><br>
		<li>» izdaja uradnih diplom za opravljene izpite,</li><br>
		<li>» določanje višine izpitnih taks za šolske in mojstrske stopnje,</li><br>
		<li>» organiziranje karate kampov in karate šol na katerih se intenzivno trenirajo izpitni programi,</li><br>
		<li>» določanje pogojev za pristop k izpitu za pasove in licence izpraševalcev,</li><br>
		<li>» overitev diplom, ki niso pridobljene na osnovi veljavnega pravilnika KK Koper ter niso pridobljene po izpitnem programu KK Koper,</li><br>
		<li>» izdelava predlogov in ostalih aktov v pristojnosti IK.</li><br>
	</ul>
	<h4 align="center">15. Člen</h4><br>
	<p>Izpraševalec za nazive od 9. KYU-a do 4. DAN v KK Koper, je g. <strong><u>Milenko Railić 6. DAN višji karate trener</u></strong>. Člana komisije pa sta <strong><u>Mladen Railić 2. DAN</u></strong> in <strong><u>Gabrijel Franca 2. DAN</u></strong>.
	</p><br>
	<h4 align="center">16. Člen</h4><br>
	<p>Za naziv 4. DAN mora IK šteti tri (3) člana, kjer mora predsednik komisije imeti najmanj <u>šesto (6) stopnjo izobrazbe</u> in stopnjo pasu <u>6. DAN</u>, druga dva člana pa najmanj <u>peto (5) stopnjo izobrazbe</u> in stopnjo pasu <u>3. DAN</u>. V spodnji razpredelnici so nazivi izpraševalcev za pasove in nivo njihove licence glede na njihovo usposobljenost in stopnjo pasu. Licence veljajo za Fudokan-Shotokan zvezo Slovenije, kjer so izpiti pridobljeni pri izpraševalcih te zveze so veljavni tudi v Karate zvezi Slovenije.
	</p><br>
