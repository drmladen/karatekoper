<div class="text">
	<p class="font-size"><br>Na podlagi 11. In 20. člena pravilnika Karate kluba Koper je izvršni odbor na svoji seji dne 21.03.2011 s spremembami in dopolnitvi sprejel naslednji</p><br><br>
	<h1 align="center">DISCIPLINSKI PRAVILNIK</h1><br><br>
	<h3><strong>I. SPLOŠNE DOLOČBE</strong></h3><br>
	<h4 align="center"><strong>1. Člen</strong></h4><br>
	<p>Ta pravilnik določa disciplinske prestopke in ukrepe za kaznovanje članov Karate kluba Koper (v nadaljevanju KK Koper), ki kršijo disciplino ali ne spoštujejo določb pravil ali drugih splošnih aktov kluba ali na kak drug način škodujejo ugledu kluba in njegovih posameznih članov.
	</p><br>
	<h4 align="center"><strong>2. Člen</strong></h4><br>
	<p>Določbe tega pravilnika veljajo za vse člane in funkcionarje kluba.
	</p><br>
	<h4 align="center"><strong>3. Člen</strong></h4><br>
	<p>Ukrepi, ki se izrečejo na podlagi tega pravilnika, veljajo samo za tisto vrsto nedovoljenih dejanj zaradi katerih je posamezni član kaznovan.
	</p><br>
	<h4 align="center"><strong></strong>4. Člen</h4><br>
	<p>Če disciplinski prekršek stori ekipa ali pa njen posamezni član, se ukrep izreče celotni ekipi, kot njenemu vodstvu.
	</p><br>
	<h4 align="center"><strong>5. Člen</strong></h4><br>
	<p>Pri sprejemu odločitve o izreku ukrepa se upošteva, ali je bil prestopek napravljen z naklepom ali pa je do njega prišlo iz malomarnosti. Enako se upoštevajo tudi vse olajševalne in obremenilne okoliščine storilca ali storilcev.
	</p><br>
	<h4 align="center"><strong>6. Člen</strong></h4><br>
	<p>Olajševalne okoliščine in obremenilne okoliščine so naslednje:<br><br>
	<ul class="bulen">
		<li>1. Olajševalne: priznanje prestopka, kesanje, dobro vedenje po storjenem prestopku, prejšnje nekaznovanje in zavzetost pri delu v klubu;</li><br>
		<li>2. Obremenilne: prejšnje kaznovanje, istovetnost prestopka, brezobzirnost, nizkotne pobude in neprimerno vedenje po prestopku.</li>
	</ul>
	</p><br><br>
	<h3><strong>II. DISCIPLINSKI PRESTOPKI IN KAZNI</strong></h3><br>
	<h4 align="center"><strong>7. Člen</strong></h4><br>
	<p>Disciplinski prestopek je vsako dejanje (storitev ali opustitev), storjeno z namenom ali z veliko malomarnostjo, ovirati delovanje kluba, ki je v skladu s pravili in drugimi splošnimi akti kluba, kakor tudi vsako dejanje v škodo kluba, ter dejanje, ki ni v skladu z načeli kodeksa karateista in športnika predpiše izvršni odbor kluba, sprejme pa ga skupščina kluba.
	</p><br>
	<h4 align="center"><strong>8. Člen</strong></h4><br>
	<p>Disciplinski prestopki članov in funkcionarjev kluba so naslednji:<br><br>
	<ul class="bulen">
		<li>» Kršitev statuta, pravilnikov in drugih splošnih aktov kluba, ter sklepov njegovih organov,</li><br>
		<li>» Nevestno in lahkomiselno izvrševanje sprejetih zadolžitev in funkcij v organih kluba,</li><br>
		<li>» Neupravičeno izostajanje od sej organov kluba,</li><br>
		<li>» Površno organiziranje in slabo vodenje prvenstvenih tekem, ki jih prireja klub,</li><br>
		<li>» Nekulturno in nedostojno vedenje (npr. zmerjanje, pretepanje, žaljive ali nedostojne geste, vinjenost, predrznost in neizpolnjevanje odredb sodnika ali drugih funkcionarjev v prostorih kluba ali ob kakršnikoli drugih pritrditvah doma in na tujem),</li><br>
		<li>» Neopravičen izostanek na povabilo disciplinskega organa,</li><br>
		<li>» Neopravičen odstop od tekmovanja in neopravičeni izostanki od reprezentančnih priprav, kot člana reprezentance, s katerim je bila sklenjena pogodba o nastopanju za klub ali državno reprezentanco,</li><br>
		<li>» Kršitev javnega reda in miru na karate tekmovanju, karate prireditvi, klubskih ali reprezentančnih treningih,</li><br>
		<li>» Vnaprej dogovorjeni izid borbe na tekmovanju in vsi drugi podobni postopki, ki vplivajo na nepravilen potek bodisi tekmovanja, izida borb ali kat.</li>
	</ul>
	</p><br>
	<h4 align="center"><strong>9. Člen</strong></h4><br>
	<p>Ukrepi za disciplinske prestopke funkcionarjev in članov kluba so naslednje:<br><br>
	<ul class="bulen">
		<li>» Opomin,</li><br>
		<li>» Javni opomin s posledico prepovedi izvrševanja funkcije ali druge dejavnosti v okviru kluba,</li><br>
		<li>» Izključitev iz članstva kluba za razdobje od enega leta z določitvijo višine denarne kazni.</li>
	</ul>
	<br>Če je prestopek storil član organa kluba ali kolektivni organ kluba, lahko disciplinski organ predlaga organu , ki je storilca izvolil, da ga razreši.<br><br>
	Organ, ki je prejel predlog iz prejšnjega odstavka tega člena mora o tem sprejeti ustrezen sklep.
	</p><br>
	<h4 align="center"><strong>10. Člen</strong></h4><br>
	<p>Ukrep, ki se izreče v obliki javnega opomina s posledico prepovedi izvrševanja funkcije ali druge dejavnosti v okviru kluba, velja za časovno razdobje najmanj enega meseca in največ dveh let.
	</p><br>
	<h4 align="center"><strong>11. Člen</strong></h4><br>
	<p>Ukrepi so tudi lahko pogojni. Pogojni ne morejo biti naslednji ukrepi: javni opomin s posledico prepovedi izvrševanja funkcije ali druge dejavnosti, če se izreče za razdobje več kakor eno leto in izključitev iz članstva.
	</p><br>
	<h4 align="center"><strong>12. Člen</strong></h4><br>
	<p>Če se disciplinsko kaznovani storilec med prestajanjem ukrepa vede vzorno, se mu kazen lahko oprosti ali zmanjša. O tem odloča disciplinski organ na pobudo izvršnega odbora kluba.
	</p><br>
	<h4 align="center"><strong>13. Člen</strong></h4><br>
	<p>Vsaka pravnomočna kazen se mora vpisati v evidenco kazni in člansko izkaznico, ki jo vodi klub.
	</p><br><br>
	<h3><strong>III DISCIPLINSKI POSTOPEK</strong></h3><br>
	<h4 align="center"><strong>14. Člen</strong></h4><br>
	<p>Disciplinski postopek se začne s prijavo. Prijava se vlaga pri disciplinskemu organu, ki je dolžan v roku 15 dni od prejema prijave odločiti o začetku disciplinskega postopka ali zavreči prejeto prijavo.
	</p><br>
	<h4 align="center"><strong>15. Člen</strong></h4><br>
	<p>Disciplinski postopek se začne na prijavo funkcionarja ali drugega člana kluba, pa tudi na prijavo vsakega prizadetega občana, ki ni član kluba.
	</p><br>
	<h4 align="center"><strong>16. Člen</strong></h4><br>
	<p>Disciplinski postopek se sproži tudi na predlog izvršnega odbora kluba ali na lastno pobudo disciplinskega organa, če ugotovijo hujšo kršitev v organih kluba.
	</p><br>
	<h4 align="center"><strong>17. Člen</strong></h4><br>
	<p>Če od trenutka storitve mine leto dni, se postopek ne more več začeti.
	</p><br>
	<h4 align="center"><strong>18. Člen</strong></h4><br>
	<p>Nihče ne more biti kaznovan, če ni bil prej zaslišan in mu ni bila dana možnost obrambe, razen v primeru, ki je naveden kot izjema v 19. Členu tega pravilnika.
	</p><br>
	<h4 align="center"><strong>19. Člen</strong></h4><br>
	<p>Če se storilec prestopka ne odzove zaslišanju in če tudi na drugo povabilo, ki mu je bilo pravilno vročeno ne pride na zaslišanje ali ne poda zahtevane pismene izjave, se ravna le na podlagi zbranih dokazov in odloča brez zaslišanja storilca.
	</p><br>
	<h4 align="center"><strong>20. Člen</strong></h4><br>
	<p>Disciplinski organ kluba odloča po prosti presoji glede na dokazni material in na vse okoliščine, v katerih je prišlo do prestopka.
	</p><br>
	<h4 align="center"><strong>21. Člen</strong></h4><br>
	<p>Disciplinski organ ima tri člane, ki jih voli skupščina. Disciplinski organ dela v sestavu treh članov. Na svojem prvem sestanku disciplinski organ izvoli predsednika, ki vodi delo disciplinskega organa.
	</p><br>
	<h4 align="center"><strong>22. Člen</strong></h4><br>
	<p>V posebno hudih primerih lahko disciplinski organ odloča tako, da začasno suspendira
	storilca, še preden se pravnomočno konča disciplinski postopek.
	</p><br>
	<h4 align="center"><strong>23. Člen</strong></h4><br>
	<p>Disciplinski organ kluba sprejema svoje odločitve z večinskim glasovanjem v pismeni obliki in jih z obrazložitvijo vrača prizadetim osebam.<br>
	Vsaka odločba disciplinskega organa mora vsebovati:<br><br>
	<ul class="bulen">
		<li>» V uvodne delu odločbe sestav disciplinskega organa,</li><br>
		<li>» Vrsto prestopka,</li><br>
		<li>» Višino kazni,</li><br>
		<li>» Obrazložitev kazni z navedbo člena, po katerem je izrečena, in datum kdaj začenja veljati.</li><br>
		<li>» Odločba mora vsebovati navedbo, s kolikšnim številom glasov članov disciplinske komisije je bila obsodba sprejeta. Za sprejem je potrebna večina sprejetih glasov prisotnih članov disciplinske komisije.</li>
	</ul>
	</p><br>
	<h4 align="center"><strong>24. Člen</strong></h4><br>
	<p>Kaznovani ima pravico, da se proti vsaki odločbi disciplinskega organa pritoži v roku 15 dni od dneva prejema pismene odločbe na sedež kluba.<br> Pritožba se vloži pri disciplinskem organu, oz. sedežu kluba, ki je izdal odločbo.<br> Disciplinski organ je dolžan pritožbo v roku 8 dni s spisi vred dostaviti organu, ki odloča o disciplinskih prestopkih na drugi stopnji. Drugostopenjski organ mora pritožbo obravnavati in jo rešiti na svoji prvi redni seji.<br> Drugostopenjski organ je izvršni odbor kluba.
	</p><br>
	<h4 align="center"><strong>25. Člen</strong></h4><br>
	<p>Na drugi stopnji se lahko odloča na naslednji način:<br><br>
	<ul class="bulen">
		<li>» Pritožba se zavrne in potrdi odločba prve stopnje,</li><br>
		<li>» Odločba se razveljavi in zadeva vrne organu prve stopnje v ponovno razpravljanje,</li><br>
		<li>» Pritožbi se ugodi samo delno in se ukrep spremeni,</li><br>
		<li>» Pritožbi se ugodi in se kaznovani oprosti.</li>
	</ul>		
	</p><br>
	<h4 align="center"><strong>26. Člen</strong></h4><br>
	<p>Proti drugostopenjski odločbi ni več mogoče vložiti pritožbe.
	</p><br>
	<h4 align="center"><strong>27. Člen</strong></h4><br>
	<p>Proti suspenzu ni mogoče vložiti pritožbe.
	</p><br>
	<h4 align="center"><strong>28. Člen</strong></h4><br>
	<p>Pritožba proti odločbi na prvi stopnji odloži izvršitev ukrepa.
	</p><br>
	<h4 align="center"><strong>29. Člen</strong></h4><br>
	<p>V roku dveh let po izteku ukrepa je kaznovani avtomatski rehabilitiran.
	</p><br>
	<h4 align="center"><strong>30. Člen</strong></h4><br>
	<p>Če kaznovani v času ukrepa ali najkasneje v roku dveh let po njegovem izteku pride do novih dejstev, ki lahko vplivajo na znižanje kazni, na njegovo ukinitev ali na popolno rehabilitacijo kaznovanega, lahko le-ta vloži predlog za obnovo postopka. O predlogu za obnovo postopka odloča disciplinski organ kluba.
	</p><br>
	<h4 align="center"><strong>31. Člen</strong></h4><br>
	<p>Med vodenjem disciplinskega postopka pristojni organi kluba uporabljajo tudi splošno
	veljavna načela zakonika o kazenskem postopku, ki niso v nasprotju s tem disciplinskim
	pravilnikom.
	</p><br>
	<h4 align="center"><strong>32. Člen</strong></h4><br>
	<p>Ta pravilnik stopi v veljavo z dnem ko ga sprejme izvršni odbor kluba.
	</p><br><br><br><br><br>
